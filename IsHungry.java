public class IsHungry {

  public static void main(String[] args) {

    int isHungry = 0;
    String food = "curry";

    System.out.println("Hi!");

    if (isHungry == 0) {
      System.out.println("I'm full.");
    } else {
      System.out.println("I'm hungry.");
      System.out.println("I'll eat " + food + ".");
    }

    System.out.println("Goodbye!");
  }
}
